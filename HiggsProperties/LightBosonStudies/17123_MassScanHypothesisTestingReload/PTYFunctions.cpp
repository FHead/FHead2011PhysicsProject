double GetPTDensity8TeVSignal_Si12621(double pt, double s)
{
   // This is from 126 GeV Higgs sample

   // "Tsallis" function
   const double m = -5.99634;
   const double n = 2.54595;
   const double n2 = 1.29154;
   const double b = 0.0159153;
   const double b2 = 0.200979;
   const double T = 0.413559;
   const double fexp = 0;

   pt = pt / sqrt(s) * 126;   // to decrease sensitivity on sqrt(s)

   // return pow(pt, n2) * exp(-b * pt) * pow(1 + (sqrt(pt * pt + m * m) - m) / (n * T), -n)
   //    + fexp * exp(-b2 * pt);

   // The second term is dropped here for speed reasons since fexp is zero!
   return pow(pt, n2) * exp(-b * pt) * pow(1 + (sqrt(pt * pt + m * m) - m) / (n * T), -n);
}
