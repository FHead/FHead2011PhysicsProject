double PDFLinearInterpolate(const double x, const int BinCount,
   const double Min, const double Max, const double *Data)
{
   if(x <= Min || x > Max) 
      return 0;

   double BinSize = (Max - Min) / BinCount;
   int Bin = (int)((x - Min) / BinSize);
   int BinL = ((Bin - 1 < 0) ? 0 : Bin - 1);
   int BinR = ((Bin + 1 >= BinCount) ? BinCount - 1 : Bin + 1);

   if(x < Min + (Bin + 0.5) * BinSize)
      BinR = Bin;
   else
      BinL = Bin;

   double DL = x - (Min + (BinL + 0.5) * BinSize);
   double DR = (Min + (BinR + 0.5) * BinSize) - x;

   double HL = ((DL > 0) ? Data[BinL] : 0);
   double HR = ((DR > 0) ? Data[BinR] : 0);

   if(DL <= 0)   DL = DL + BinSize;
   if(DR <= 0)   DR = DR + BinSize;

   if(DL + DR == 0)
      return 0;

   return (HL * DR + HR * DL) / (DL + DR);
}
