double ToyPTDensity(double pt)
{
   double Value = 0;

   if(pt < 10)
      Value = (-3.38 + pt * 117.642 - pt * pt * 7.454) * 10 / 4276.4 * 4700.68 / 5200;
   else if(pt > 0)
      Value = (6833 * exp(-0.0385 * pt) + 57.226 * exp(-0.0112 * pt)) / 5200;

   if(Value < 0)
      Value = 0;

   return Value;
}
